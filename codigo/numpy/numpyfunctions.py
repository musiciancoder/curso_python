import numpy as np

values = np.arange(0, 10, 1)
print(values)

print()

print(np.sin(values))
print(np.min(values))
print(np.max(values))
print(np.mean(values)) # devuelve la media, o sea el promedio
print(np.std(values)) # devuelve la desviacion estandar
print(np.var(values)) # devuelve la varianza
print(np.sum(values)) # devuelve la suma
print()
print(values.min()) # igual funciona
print(values.mean())  # igual funciona


